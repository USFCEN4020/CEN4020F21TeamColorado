# NO RETURN VALUE: all functions are print statements full of text blocks
def importantLinks():
    while True:
        print("                       InCollege - Important Links                         ")
        print("Please select one of the following options to view the associated document.")
        print("""
            1. Copyright Notice
            2. About
            3. Accessibility
            4. User Agreement
            5. Privacy Policy
            6. Cookie Policy
            7. Brand Policy

            8. Return Home
        """)
        try:
            newOption = int(input("Choice: "))
            print()
            # Copyright Notice option
            if newOption == 1:
                copyrightNotice()
            # About option
            elif newOption == 2:
                aboutUs()
            # Accessibility option
            elif newOption == 3:
                accessibilityPage()
            # User Agreement option
            elif newOption == 4:
                userAgreement()
            # Privacy Policy option
            elif newOption == 5:
                privacyPolicy()
            # Cookie Policy option
            elif newOption == 6:
                cookiePolicy()
            # Brand Policy option
            elif newOption == 7:
                brandPolicy()
            # Return home option
            elif newOption == 8:
                print("Returning to home page...")
                break
        except ValueError:
            print("You provided a non-integer value.")
    